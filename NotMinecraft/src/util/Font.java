	public float[] getChar(char ch) {
		int c = (int) ch - 32;
		return new float[] {uv[c][0]/128f, uv[c][1]/128f,
							uv[c][2]/128f, uv[c][3]/128f,
							uv[c][2] - uv[c][0],
							uv[c][3] - uv[c][1]};
	}
	
